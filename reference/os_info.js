const os = require("os");

// PLatform
console.log(os.platform());

// CPU Arch

console.log(os.arch());

// CPU CORE INFO
console.log(os.cpus());

// Free memory

console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Home directory
console.log(os.homedir());

// Uptime
console.log(os.uptime());
